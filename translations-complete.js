// Language switcher
let currentLang = 'en';
const originalTexts = new Map();

function translatePage() {
    if (currentLang === 'ar') {
        // Translate to Arabic
        document.querySelectorAll('*').forEach(element => {
            // Skip scripts, styles, and already processed elements
            if (element.tagName === 'SCRIPT' || element.tagName === 'STYLE' || element.classList.contains('lang-toggle')) {
                return;
            }
            
            // Get direct text nodes only
            Array.from(element.childNodes).forEach(node => {
                if (node.nodeType === Node.TEXT_NODE) {
                    const text = node.textContent.trim();
                    if (text && completeTranslations[text]) {
                        if (!originalTexts.has(node)) {
                            originalTexts.set(node, text);
                        }
                        node.textContent = node.textContent.replace(text, completeTranslations[text]);
                    }
                }
            });
            
            // Handle data attributes
            if (element.dataset.en && element.dataset.ar) {
                if (!originalTexts.has(element)) {
                    originalTexts.set(element, element.textContent);
                }
                element.textContent = element.dataset.ar;
            }
        });
    } else {
        // Restore English
        originalTexts.forEach((originalText, node) => {
            if (node.nodeType === Node.TEXT_NODE) {
                node.textContent = originalText;
            } else if (node.dataset && node.dataset.en) {
                node.textContent = node.dataset.en;
            }
        });
        originalTexts.clear();
    }
}

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'ar' : 'en';
    document.documentElement.lang = currentLang;
    document.body.dir = currentLang === 'ar' ? 'rtl' : 'ltr';
    
    const langToggle = document.getElementById('langToggle');
    langToggle.textContent = currentLang === 'en' ? 'Ø§Ù„Ø¹Ø±Ø¨ÙŠØ©' : 'English';
    
    translatePage();
    
    localStorage.setItem('preferredLanguage', currentLang);
}

document.addEventListener('DOMContentLoaded', () => {
    const langToggle = document.getElementById('langToggle');
    if (langToggle) {
        langToggle.addEventListener('click', toggleLanguage);
    }
    
    const savedLang = localStorage.getItem('preferredLanguage');
    if (savedLang === 'ar') {
        currentLang = 'en'; // Set to en first
        toggleLanguage(); // Then toggle to ar
    }
});
